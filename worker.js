const threads = new Map();
let nextId = 1;
let totalCost = 12345;

// ===== Router =====
export default {
  async fetch(request) {
    const url = new URL(request.url);
    const path = url.pathname;

    if (request.method === "OPTIONS") return new Response(null, { headers: CORS_HEADERS });

    // Generate
    if (path === "/api/bbs/generate" && request.method === "POST") {
      const { title, body } = await request.json();
      if (!title || !body) return json({ error: "\u30BF\u30A4\u30C8\u30EB\u3068\u672C\u6587\u3092\u5165\u529B\u3057\u3066\u304F\u3060\u3055\u3044" }, 400);
      return json({ replies: generateReplies(title, body), isSafetyMode: false, isSensitive: false });
    }

    // Continue
    if (path === "/api/bbs/continue" && request.method === "POST") {
      const { title, body, userReply, currentTotal } = await request.json();
      const count = Math.floor(Math.random() * 5) + 3;
      const replies = generateReplies(title, body).slice(0, count).map((r, i) => ({ ...r, number: (currentTotal || 2) + i + 1 }));
      return json({ replies, reachedLimit: (currentTotal || 0) + replies.length >= 30 });
    }

    // Save log
    if (path === "/api/bbs/logs" && request.method === "POST") {
      const data = await request.json();
      const id = nextId++;
      threads.set(id, { ...data, createdAt: new Date().toISOString() });
      return json({ id });
    }

    // Get logs
    if (path === "/api/bbs/logs" && request.method === "GET") {
      const logs = [];
      threads.forEach((v, k) => logs.push({ id: k, ...v }));
      return json(logs.sort((a, b) => b.id - a.id));
    }

    // Public threads
    if (path === "/api/bbs/public-threads" && request.method === "GET") {
      const q = (url.searchParams.get("q") || "").toLowerCase();
      const list = [];
      threads.forEach((v, k) => { if (v.isPublic && (!q || v.title.toLowerCase().includes(q))) list.push({ id: k, ...v }); });
      return json({ threads: list.sort((a, b) => b.id - a.id) });
    }

    // Config
    if (path === "/api/bbs/config") return json({ maxTitleLength: 50, maxBodyLength: 1000, maxReplies: 30, freeGenerationsPerDay: 5 });

    // Counter
    if (path === "/api/counter") { totalCost += Math.floor(Math.random() * 5); return json({ deficit: totalCost }); }

    // Stubs
    if (path.startsWith("/api/")) return json({ ok: true });

    return json({ error: "not found" }, 404);
  }
};
